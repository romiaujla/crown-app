import { execFile } from "node:child_process";
import { mkdir, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { promisify } from "node:util";

const execFileAsync = promisify(execFile);

const dirname = path.dirname(fileURLToPath(import.meta.url));
const appRoot = path.resolve(dirname, "..");
const schemaPath = path.join(appRoot, "prisma", "transportation-management-system-schema.prisma");
const outputDir = path.join(appRoot, "tenant-migrations", "0001_base");
const outputPath = path.join(outputDir, "001_foundational_tms_schema.sql");

const { stdout } = await execFileAsync(
  "pnpm",
  ["exec", "prisma", "migrate", "diff", "--from-empty", "--to-schema-datamodel", schemaPath, "--script"],
  {
    cwd: appRoot,
    maxBuffer: 10 * 1024 * 1024
  }
);

const sql = [
  "-- Generated by pnpm db:tenant:migration:generate",
  "-- Source: apps/api/prisma/transportation-management-system-schema.prisma",
  stdout.trim(),
  ""
].join("\n");

await mkdir(outputDir, { recursive: true });
await writeFile(outputPath, sql, "utf8");

process.stdout.write(`${outputPath}\n`);
